#!/usr/bin/python3
"""
This module provides a simple addition function.

It exports a single function:
    - add(a, b): Returns the sum of two floating-point numbers.

Intended for basic arithmetic operations or as a utility function in larger programs.
"""

def add(a: float, b: float) -> float:
    """Return the sum of a and b.

    Args:
        a (float): The first number to add.
        b (float): The second number to add.

    Returns:
        float: The result of a + b.
    """
    return a + b